class Solution{
public:
    int evenlyDivides(int N){
        //code here
        int b=N;
        int count=0;
        while(b){
            int x=b%10;
            if(x==0){b=b/10;continue;}
            if(N%x==0)count++;
            b=b/10;
        }
        return count;
    }
};

