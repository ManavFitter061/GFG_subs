/*

The structure of linked list is the following

struct Node
{
    int data;
    Node* next;
};

*/
class Solution
{
    public:
    
    struct Node * mergeResult(Node *node1,Node *node2)
    {
        if (node1 == NULL and node2 == NULL)
        {
            return NULL;
        }
        vector<int> v;
        Node * temp = node1;
        while(temp != NULL)
        {
            v.push_back(temp->data);
            temp = temp->next;
        }
        temp = node2;
        while(temp != NULL)
        {
            v.push_back(temp->data);
            temp = temp->next;
        }
        
        sort(v.begin(), v.end());
        Node * ans = newNode(v[v.size()-1]);
        temp = ans;
        for (int i=v.size()-2;i>=0;i--)
        {
            Node * z = newNode(v[i]);
            temp->next = z;
            temp=temp->next;
        }
        return ans;
    }
};


