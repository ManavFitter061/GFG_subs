class Solution {
  public:
    long long solve(vector<int>& bt) {
        // code here
        sort(bt.begin(),bt.end());
        int n=bt.size();
        int mul=n-1;
        long long int ans=0;
        for(int i=0;i<n-1;i++){
            ans+=(mul*bt[i]*1LL);
            mul--;
            // cout<<ans<<endl;
        }
        return (ans/n);
    }
};

